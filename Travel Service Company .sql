--1.Write a sql query that gives the below output
Select Segment, count (distinct u.User_id) as TotalUserCount,
count(distinct case when Line_of_business ='Flight' and month(Booking_date) = 4 then b.user_id  end) as User_with_book_flight_In_2022
from user_table u left join booking_table b
on u.User_id = b.User_id
group by Segment




--2. write a query to identfy users whose first booking was a hotal booking
with cte as(
Select *, ROW_NUMBER() over(Partition by User_id order by booking_date) as rn
from booking_table)
select * from cte
where rn = 1 and Line_of_business = 'Hotel'

--OR

select distinct User_id from (
Select *, first_Value(Line_of_business) over(Partition by User_id order by booking_date) as first_booking
from booking_table)a
where  first_booking = 'Hotel'

--3. Write a query to calculate the days between first and last boofing of each user

Select user_id, DATEDIFF(DAY,min(booking_date),max(booking_date) ) as Diff_firstlastbooking
from booking_table
group by User_id


--4. Write a query to count the number of flight and hotel booking in each of the usersegments for the year 2022

Select Segment,
count(case when Line_of_business ='Flight' then Booking_id end) as Flight_booking,
count(case when Line_of_business ='Hotel' then Booking_id end) as Hotel_booking
from booking_table b inner join user_table u on
b.user_id =u.user_id
group by Segment
 

